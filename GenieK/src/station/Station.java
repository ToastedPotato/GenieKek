package station;

public class Station {

    protected String id, name, city;

    public void setName(String name){
        this.name = name;
    }
    
    public String getName(){
        return this.name;
    }
    
    public void setId(String id) {
        this.id = id;
    }
    
    public String getId(){
        return this.Id;
    }
    
    public void setCity(String city) {
        this.city = city;
    }
    
    public String getCity(){
        return this.City;
    }
}
